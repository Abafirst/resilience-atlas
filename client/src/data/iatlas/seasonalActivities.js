export const HOLIDAY_PACK_LIST = Object.values(HOLIDAY_PACKS);

/**
 * Get all activities across all seasonal packs.
 * @returns {Array}
 */
export function getAllSeasonalActivities() {
  return SEASONAL_PACK_LIST.flatMap(pack =>
    pack.activities.map(act => ({ ...act, packId: pack.id, packName: pack.name, season: pack.id }))
  );
}

/**
 * Get activities for a specific season.
 * @param {'spring'|'summer'|'fall'|'winter'} season
 * @returns {Array}
 */
export function getSeasonalActivities(season) {
  return SEASONAL_PACKS[season]?.activities || [];
}
