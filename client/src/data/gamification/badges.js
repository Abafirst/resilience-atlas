/** All badges combined */
export const ALL_BADGES = [...ALL_DIMENSION_BADGES, ...UNIVERSAL_BADGES];

/** Get all badges for a specific dimension */
export function getBadgesForDimension(dimensionKey) {
  return ALL_DIMENSION_BADGES.filter(b => b.dimension === dimensionKey);
}

/** Get a badge by ID */
export function getBadgeById(id) {
  return ALL_BADGES.find(b => b.id === id) || null;
}
