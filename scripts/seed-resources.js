function makeSlug(title) {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 100);
}

function r(fields) {
  return { status: 'published', publishedAt: new Date(), timeCommitment: 10, ...fields };
}

