// ── Main seed function ─────────────────────────────────────────────────────────

async function run() {
  const uri = process.env.MONGODB_URI;
  if (!uri) {
    console.error('ERROR: MONGODB_URI environment variable is required.');
    console.error('Usage: MONGODB_URI=<uri> node scripts/seed-resources.js');
    process.exit(1);
  }

  console.log('Connecting to MongoDB...');
  await mongoose.connect(uri);
  console.log('Connected.\n');

  // Require model AFTER mongoose.connect
  const Resource = require(path.join(__dirname, '..', 'backend', 'models', 'Resource'));

  let inserted = 0;
  let updated  = 0;
  let errors   = 0;

  for (const data of RESOURCES) {
    const resourceSlug = data.slug || makeSlug(data.title);

    const filter = { slug: resourceSlug };
    const update = { $set: { ...data, slug: resourceSlug } };
    const opts   = { upsert: true, new: true, setDefaultsOnInsert: true };

    try {
      const prev = await Resource.findOne(filter).select('_id createdAt').lean();
      await Resource.findOneAndUpdate(filter, update, opts);
      if (prev) {
        updated++;
        console.log(`  updated:  ${data.title}`);
      } else {
        inserted++;
        console.log(`  inserted: ${data.title}`);
      }
    } catch (err) {
      errors++;
      console.error(`  error for "${data.title}": ${err.message}`);
    }
  }

  console.log('\n----------------------------------------');
  console.log('Seed complete:');
  console.log(`  Inserted: ${inserted}`);
  console.log(`  Updated:  ${updated}`);
  console.log(`  Errors:   ${errors}`);
  console.log(`  Total:    ${RESOURCES.length}`);
  console.log('----------------------------------------\n');

  await mongoose.disconnect();
  process.exit(0);
}

run().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
